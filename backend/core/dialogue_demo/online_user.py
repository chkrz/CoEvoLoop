from typing import Dict, Any, List
from core.utils.json_utils import parse_json
from core.onemodel.user.user import UserSimulatorPortrait



async def online_generate_user_input(
    user_data: Dict[str, Any],
    history: List[Dict[str, Any]],
    turn: int
):
    try:
        end = False
        user_simulator = UserSimulatorPortrait(
            user_portrait=user_data["portrait"]
        )

        user_response = await user_simulator.query_user(conversation=history)
        user_response = parse_json(user_response)
        assert "content" in user_response and "end_conversation" in user_response

        if user_simulator.check_ending(user_response):
            end = True
            return {"user_input": None, "end": end}

        user_input = user_response["content"]

        # if turn == 0:
        #     return {"user_input": user_data["example_query"], "end": False}
    except Exception as e:
        user_input = "系统繁忙中，请稍后重试"
        end = False

    return {"user_input": user_input, "end": end}


